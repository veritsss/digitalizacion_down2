    document.getElementById('rut').addEventListener('input', function () {
        const rut = this.value;
        const regex = /^\d{1,2}\.\d{3}\.\d{3}-[0-9kK]{1}$/;

        if (!regex.test(rut)) {
            this.setCustomValidity('El RUT debe tener el formato 11.111.111-1');
        } else {
            this.setCustomValidity('');
        }
    });
</script>
